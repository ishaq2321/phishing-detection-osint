def _makeResponse(**overrides) -> AnalysisResponse:
    """Build a minimal valid AnalysisResponse."""
    defaults = dict(
        success=True,
        verdict=VerdictResult(
            isPhishing=False,
            confidenceScore=0.2,
            threatLevel="safe",
            reasons=[],
            recommendation="Looks safe",
        ),
        features=FeatureSummary(),
        analysisTime=100.0,
        analyzedAt=datetime.now(),
    )
    defaults.update(overrides)
    return AnalysisResponse(**defaults)


@pytest.fixture()
def store() -> HistoryStore:
    """Fresh empty store for each test."""
    return HistoryStore(maxEntries=MAX_ENTRIES)


@pytest.fixture()
def smallStore() -> HistoryStore:
    """Store with a small capacity for FIFO testing."""
    return HistoryStore(maxEntries=5)


