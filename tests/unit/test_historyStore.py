class TestHistoryEntry:
    def test_idAutoGenerated(self):
        entry = HistoryEntry(
            content="https://example.com",
            contentType="url",
            response=_makeResponse(),
        )
        assert entry.id
        assert len(entry.id) == 36  # UUID format

    def test_createdAtAutoSet(self):
        entry = HistoryEntry(
            content="test",
            contentType="text",
            response=_makeResponse(),
        )
        assert isinstance(entry.createdAt, datetime)

    def test_uniqueIds(self):
        ids = set()
        for _ in range(20):
            entry = HistoryEntry(
                content="x", contentType="text", response=_makeResponse()
            )
            ids.add(entry.id)
        assert len(ids) == 20


