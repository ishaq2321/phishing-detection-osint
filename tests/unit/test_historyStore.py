class TestHistoryEndpoints:
    """Test history REST endpoints via FastAPI TestClient."""

    @pytest.fixture(autouse=True)
    def _setupClient(self):
        """Import the app and reset the store before each test."""
        from fastapi.testclient import TestClient
        from backend.api.historyStore import historyStore
        from backend.main import app

        historyStore.clear()
        self.client = TestClient(app)
        self.store = historyStore

    def test_listHistoryEmpty(self):
        resp = self.client.get("/api/history")
        assert resp.status_code == 200
        data = resp.json()
        assert data["entries"] == []
        assert data["total"] == 0

    def test_analyzeAutoSavesToHistory(self):
        self.client.post(
            "/api/analyze/url",
            json={"url": "https://example.com"},
        )
        resp = self.client.get("/api/history")
        assert resp.status_code == 200
        data = resp.json()
        assert data["total"] == 1
        assert data["entries"][0]["content"] == "https://example.com"

    def test_getHistoryEntryById(self):
        self.client.post(
            "/api/analyze/url",
            json={"url": "https://test.com"},
        )
        entries = self.client.get("/api/history").json()["entries"]
        entryId = entries[0]["id"]

        resp = self.client.get(f"/api/history/{entryId}")
        assert resp.status_code == 200
        assert resp.json()["id"] == entryId

    def test_getHistoryEntryNotFound(self):
        resp = self.client.get("/api/history/non-existent-id")
        assert resp.status_code == 404

    def test_deleteHistoryEntry(self):
        self.client.post(
            "/api/analyze/url",
            json={"url": "https://delete-me.com"},
        )
        entries = self.client.get("/api/history").json()["entries"]
        entryId = entries[0]["id"]

        resp = self.client.delete(f"/api/history/{entryId}")
        assert resp.status_code == 200
        assert resp.json()["deleted"] is True

        assert self.client.get(f"/api/history/{entryId}").status_code == 404

    def test_deleteHistoryEntryNotFound(self):
        resp = self.client.delete("/api/history/non-existent-id")
        assert resp.status_code == 404

    def test_clearHistory(self):
        for i in range(3):
            self.client.post(
                "/api/analyze/url",
                json={"url": f"https://url-{i}.com"},
            )
        resp = self.client.delete("/api/history")
        assert resp.status_code == 200
        data = resp.json()
        assert data["deleted"] is True
        assert data["count"] == 3
        assert self.client.get("/api/history").json()["total"] == 0

    def test_historyPagination(self):
        for i in range(5):
            self.client.post(
                "/api/analyze/url",
                json={"url": f"https://page-{i}.com"},
            )
        resp = self.client.get("/api/history?limit=2&offset=0")
        data = resp.json()
        assert len(data["entries"]) == 2
        assert data["total"] == 5

    def test_analyzeEmailSavesToHistory(self):
        self.client.post(
            "/api/analyze/email",
            json={
                "content": "Your account has been suspended",
                "subject": "Alert",
                "sender": "test@example.com",
            },
        )
        data = self.client.get("/api/history").json()
        assert data["total"] == 1
        assert data["entries"][0]["contentType"] == "email"

    def test_analyzeContentAutoSavesToHistory(self):
        self.client.post(
            "/api/analyze",
            json={"content": "Some text content", "contentType": "text"},
        )
        data = self.client.get("/api/history").json()
        assert data["total"] == 1

    def test_rootEndpointIncludesHistoryLink(self):
        resp = self.client.get("/api/")
        data = resp.json()
        assert "history" in data["endpoints"]
