class TestHistoryStoreAdd:
    def test_addReturnsEntry(self, store: HistoryStore):
        entry = store.add("https://example.com", "url", _makeResponse())
        assert isinstance(entry, HistoryEntry)
        assert entry.content == "https://example.com"
        assert entry.contentType == "url"

    def test_addIncrementsCount(self, store: HistoryStore):
        assert store.count == 0
        store.add("a", "url", _makeResponse())
        assert store.count == 1
        store.add("b", "url", _makeResponse())
        assert store.count == 2

    def test_addFifoEviction(self, smallStore: HistoryStore):
        entries = []
        for i in range(7):
            entries.append(smallStore.add(f"url-{i}", "url", _makeResponse()))
        assert smallStore.count == 5
        remaining = smallStore.list()
        ids = [e.id for e in remaining.entries]
        assert entries[0].id not in ids
        assert entries[1].id not in ids
        assert entries[6].id in ids


class TestHistoryStoreList:
    def test_listEmpty(self, store: HistoryStore):
        result = store.list()
        assert result.entries == []
        assert result.total == 0

    def test_listNewestFirst(self, store: HistoryStore):
        store.add("first", "url", _makeResponse())
        store.add("second", "url", _makeResponse())
        store.add("third", "url", _makeResponse())
        result = store.list()
        assert result.entries[0].content == "third"
        assert result.entries[2].content == "first"

    def test_listWithLimit(self, store: HistoryStore):
        for i in range(10):
            store.add(f"url-{i}", "url", _makeResponse())
        result = store.list(limit=3)
        assert len(result.entries) == 3
        assert result.total == 10

    def test_listWithOffset(self, store: HistoryStore):
        for i in range(5):
            store.add(f"url-{i}", "url", _makeResponse())
        result = store.list(limit=2, offset=2)
        assert len(result.entries) == 2
        assert result.entries[0].content == "url-2"


